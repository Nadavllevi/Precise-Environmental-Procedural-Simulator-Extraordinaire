package pepse.world.avatar;

/**
 * AvatarMode Enum.
 * Represents avatar different movement modes.
 * @author Nadav Levi.
 */
public enum AvatarMode {
    /**
     * IDLE : not in movement.
     */
    IDLE,
    /**
     * RUN : moves horizontally.
     */
    RUN,
    /**
     * JUMP : moves both vertically and horizontally (optional).
     */
    JUMP;
}
